`timescale 1ns/1ps
/*
*******************************************************************************************************
File name   : key_expansion.sv
Module name : key_expansion

Korean:
- AES-128 Key Expansionм—җм„ң "нҳ„мһ¬ round key"лЎңл¶Җн„° "лӢӨмқҢ round key"лҘј мғқм„ұн•ҳлҠ” лӘЁл“Ҳмһ…лӢҲлӢӨ.
- мһ…л Ҙ key_inмқҖ нҳ„мһ¬ 128-bit round keyмһ…лӢҲлӢӨ.
- мһ…л Ҙ roundлҠ” лӢӨмқҢ keyлҘј л§Ңл“Ө л•Ң мӮ¬мҡ©н•  Rcon лІҲнҳёмһ…лӢҲлӢӨ.
  мҳҲ: key_in = RoundKey0, round = 1мқҙл©ҙ RoundKey1мқ„ мғқм„ұн•©лӢҲлӢӨ.
      key_in = RoundKey1, round = 2мқҙл©ҙ RoundKey2лҘј мғқм„ұн•©лӢҲлӢӨ.
- AES-128м—җм„ңлҠ” мҙқ 11к°ңмқҳ round keyк°Җ н•„мҡ”н•©лӢҲлӢӨ.
  RoundKey0лҠ” original keyмқҙкі , RoundKey1~RoundKey10мқҖ key expansionмңјлЎң мғқм„ұлҗ©лӢҲлӢӨ.
- мқҙ лӘЁл“ҲмқҖ мЎ°н•©л…јлҰ¬(combinational logic)мқҙлҜҖлЎң clk/resetмқҙ н•„мҡ” м—ҶмҠөлӢҲлӢӨ.

English:
- This module generates the next AES-128 round key from the current round key.
- key_in is the current 128-bit round key.
- round selects the Rcon value used to generate the next key.
  Example: key_in = RoundKey0 and round = 1 generates RoundKey1.
           key_in = RoundKey1 and round = 2 generates RoundKey2.
- AES-128 requires 11 round keys in total.
  RoundKey0 is the original key, and RoundKey1~RoundKey10 are generated by key expansion.
- This is combinational logic, so no clock or reset is required.

Important byte/word order:
- key_in[127:96] = w0
- key_in[95:64]  = w1
- key_in[63:32]  = w2
- key_in[31:0]   = w3
- key_out = {w4, w5, w6, w7}

This ordering is consistent with the AES state vector used in the previous modules.
*******************************************************************************************************
*/

module key_expansion (
    input  wire [127:0] key_in,    // Korean: нҳ„мһ¬ 128-bit round key / English: current 128-bit round key
    input  wire [3:0]   round,     // Korean: Rcon м„ нғқмҡ© round лІҲнҳё 1~10 / English: round number for Rcon selection, 1~10
    output wire [127:0] key_out    // Korean: лӢӨмқҢ 128-bit round key / English: next 128-bit round key
);

    /*
    Korean:
    - AES-128 keyлҠ” 4к°ңмқҳ 32-bit wordлЎң лӮҳлҲ•лӢҲлӢӨ.
    - мҳҲ: original key = 2b7e1516 28aed2a6 abf71588 09cf4f3c
      w0 = 2b7e1516
      w1 = 28aed2a6
      w2 = abf71588
      w3 = 09cf4f3c

    English:
    - The AES-128 key is split into four 32-bit words.
    */
    wire [31:0] w0;
    wire [31:0] w1;
    wire [31:0] w2;
    wire [31:0] w3;

    assign w0 = key_in[127:96];
    assign w1 = key_in[95:64];
    assign w2 = key_in[63:32];
    assign w3 = key_in[31:0];

    /*
    Korean:
    - лӢӨмқҢ keyмқҳ wordл“Өмһ…лӢҲлӢӨ.
    - w4 = w0 XOR SubWord(RotWord(w3)) XOR Rcon
    - w5 = w1 XOR w4
    - w6 = w2 XOR w5
    - w7 = w3 XOR w6

    English:
    - These are the next key words.
    */
    wire [31:0] w4;
    wire [31:0] w5;
    wire [31:0] w6;
    wire [31:0] w7;

    /*
    Korean:
    - RotWord(w3)лҘј мҲҳн–үн•©лӢҲлӢӨ.
    - w3 = {byte0, byte1, byte2, byte3}лқјл©ҙ
      RotWord(w3) = {byte1, byte2, byte3, byte0}

    English:
    - Perform RotWord on w3.
    - If w3 = {byte0, byte1, byte2, byte3},
      RotWord(w3) = {byte1, byte2, byte3, byte0}.
    */
    wire [7:0] rot_b0;
    wire [7:0] rot_b1;
    wire [7:0] rot_b2;
    wire [7:0] rot_b3;

    assign rot_b0 = w3[23:16];
    assign rot_b1 = w3[15:8];
    assign rot_b2 = w3[7:0];
    assign rot_b3 = w3[31:24];

    /*
    Korean:
    - SubWordлҠ” RotWord кІ°кіјмқҳ к°Ғ byteм—җ S-boxлҘј м Ғмҡ©н•ҳлҠ” лӢЁкі„мһ…лӢҲлӢӨ.
    - л”°лқјм„ң sbox module 4к°ңлҘј мӮ¬мҡ©н•©лӢҲлӢӨ.

    English:
    - SubWord applies the AES S-box to each byte of the RotWord result.
    - Therefore, four sbox modules are used.
    */
    wire [7:0] sub_b0;
    wire [7:0] sub_b1;
    wire [7:0] sub_b2;
    wire [7:0] sub_b3;

    sbox sbox_key_0 (.in_byte(rot_b0), .out_byte(sub_b0));
    sbox sbox_key_1 (.in_byte(rot_b1), .out_byte(sub_b1));
    sbox sbox_key_2 (.in_byte(rot_b2), .out_byte(sub_b2));
    sbox sbox_key_3 (.in_byte(rot_b3), .out_byte(sub_b3));

    /*
    Korean:
    - RconмқҖ roundл§ҲлӢӨ м •н•ҙм§„ 8-bit к°’мһ…лӢҲлӢӨ.
    - AES-128м—җм„ңлҠ” round 1~10к№Ңм§Җ мӮ¬мҡ©н•©лӢҲлӢӨ.
    - 32-bitлЎң м Ғмҡ©н•  л•ҢлҠ” {Rcon, 24'h000000} нҳ•нғңмһ…лӢҲлӢӨ.

    English:
    - Rcon is a fixed 8-bit value for each round.
    - AES-128 uses rounds 1 to 10.
    - In a 32-bit word, it is applied as {Rcon, 24'h000000}.
    */
    wire [7:0] rcon_value;

    assign rcon_value = get_rcon(round);

    /*
    Korean:
    - temp_word = SubWord(RotWord(w3)) XOR Rcon
    - RconмқҖ мІ« лІҲм§ё byteм—җл§Ң XORлҗ©лӢҲлӢӨ.

    English:
    - temp_word = SubWord(RotWord(w3)) XOR Rcon.
    - Rcon is XORed only with the first byte.
    */
    wire [31:0] temp_word;

    assign temp_word = {
        sub_b0 ^ rcon_value,
        sub_b1,
        sub_b2,
        sub_b3
    };

    /*
    Korean:
    - AES-128 key expansion кіөмӢқмһ…лӢҲлӢӨ.
    - w4лҘј лЁјм Җ л§Ңл“Өкі , к·ё лӢӨмқҢ w5, w6, w7мқ„ мҲңм„ңлҢҖлЎң л§Ңл“ӯлӢҲлӢӨ.

    English:
    - AES-128 key expansion equations.
    - w4 is generated first, then w5, w6, and w7.
    */
    assign w4 = w0 ^ temp_word;
    assign w5 = w1 ^ w4;
    assign w6 = w2 ^ w5;
    assign w7 = w3 ^ w6;

    /*
    Korean:
    - лӢӨмқҢ round keyлҘј 128-bitлЎң л¬¶м–ҙм„ң м¶ңл Ҙн•©лӢҲлӢӨ.
    - мқҙ мҲңм„ңлҠ” AES stateмҷҖ AddRoundKeyм—җм„ң м“°лҠ” column-major vector мҲңм„ңмҷҖ л§һм¶ҘлӢҲлӢӨ.

    English:
    - Pack the next round key into a 128-bit output.
    - This ordering matches the column-major vector order used by the AES state and AddRoundKey.
    */
    assign key_out = {w4, w5, w6, w7};

    /*
    Korean:
    - round лІҲнҳём—җ л”°лқј Rcon к°’мқ„ л°ҳнҷҳн•ҳлҠ” functionмһ…лӢҲлӢӨ.
    - roundк°Җ 1~10мқҙ м•„лӢҲл©ҙ 00мқ„ л°ҳнҷҳн•©лӢҲлӢӨ.

    English:
    - This function returns the Rcon value for each round.
    - If round is not 1~10, it returns 00.
    */
    function [7:0] get_rcon;
        input [3:0] r;
        begin
            case (r)
                4'd1:  get_rcon = 8'h01;
                4'd2:  get_rcon = 8'h02;
                4'd3:  get_rcon = 8'h04;
                4'd4:  get_rcon = 8'h08;
                4'd5:  get_rcon = 8'h10;
                4'd6:  get_rcon = 8'h20;
                4'd7:  get_rcon = 8'h40;
                4'd8:  get_rcon = 8'h80;
                4'd9:  get_rcon = 8'h1b;
                4'd10: get_rcon = 8'h36;
                default: get_rcon = 8'h00;
            endcase
        end
    endfunction

endmodule
